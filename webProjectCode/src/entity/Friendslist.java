package entity;

public class Friendslist {
    private int listId;
    private int friendId;
    private int userId;

    public int getlistId() {
        return listId;
    }
    public void setlistId(int li) {
        this.listId=li;
    }

    public int getfriendId() {
        return friendId;
    }
    public void setfriendId(int fi) {
        this.friendId=fi;
    }

    public int getuserId() {
        return userId;
    }
    public void setuserId(int ui) {
        this.userId=ui;
    }
}
